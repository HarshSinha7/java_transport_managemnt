    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection connection=con.getconnection();
        if(jTextField1.getText().length()!=0){
        try {
            PreparedStatement ps=connection.prepareStatement(query.search_vehicle);
            ps.setString(1,jTextField1.getText());
            ResultSet rs=ps.executeQuery();
            
            if(rs.isBeforeFirst()){
            while(rs.next()){
            String name=rs.getString(1);
            String father=rs.getString(2);
            String dob=rs.getString(3);
            String address=rs.getString(4);
            String vehicletype=rs.getString(5);
            String vehiclemodel=rs.getString(6);
            String registrationnumber=rs.getString(7);
            String registrationdate=rs.getString(8);
            
            
            String table[]={name,father,dob,address,vehicletype,vehiclemodel,registrationnumber,registrationdate};
            DefaultTableModel tablemodel=(DefaultTableModel) jTable1.getModel();
            tablemodel.setRowCount(0);
            tablemodel.addRow(table);
            }
            
         }else{
            JOptionPane.showMessageDialog(null,"NO FOUND");
            jTextField1.setText("");
            }
         
         } catch (SQLException ex) {
            Logger.getLogger(delete_vehicle.class.getName()).log(Level.SEVERE, null, ex);
        }
      }else if(jTextField1.getText().length()==0){
      JOptionPane.showMessageDialog(null,"PLEASE FILL THE REQUIRED FEILD");
      }
    }//GEN-LAST:event_jButton1ActionPerformed

